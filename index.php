<script>
$(function(){
	var timer;
	var ul = $('#news');
	var timeout = 3000;
	function scroll(){
		ul.animate({top:"-46px"}, 500, function(){
			ul.find('li').first().appendTo(ul);
			ul.css('top', '0');
			start();
		});
	}
	function start(){
		timer = setTimeout(function(){
			scroll();
		}, timeout);
	}
	function stop(){
		clearTimeout(timer);
	}
	ul.hover(function(){
		stop();
	},function () {
		start();
	});
	start();
});
</script>
